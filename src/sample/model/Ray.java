package sample.model;

public class Ray {
    private int startX;
    private int startY;
    private int endX;
    private int endY;

    public Ray(int startX, int startY, int endX, int endY) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
}
